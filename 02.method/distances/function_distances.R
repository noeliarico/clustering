##//////////////////////////////////////////////////////////////////////////////
## Lp Minkowski distance measures //////////////////////////////////////////////
##//////////////////////////////////////////////////////////////////////////////
  
dist_minkowski <- function(x, y, p = 2) {
  out <- .C("dist_minkowski", x = as.double(x), 
                          y = as.double(y), 
                          n = as.integer(length(x)), 
                          p = as.integer(p), 
                          result = as.double(0))
  return(out$result)
}

